// Operadores Lógicos
// Los operadores lógicos se utilizan para comparar valores entre si, y responden un true si es verdad o un false si es falso.

// Mayor que ( > )
console.log(10 > 5);
console.log(5 > 10);

// Mayor o igual que ( >= )
console.log(10 >= 10);

// Menor que ( < )
console.log(10 > 5);

// Menor o igual que ( <= )
console.log(5 >= 5);
console.log(4 >= 5);

// Igualdad ( == )
// Compara si el valor es igual
console.log(7 == 7);
console.log(7 == "7");

// Igualdad estricta( === )
// Compara si el valor y el tipo de dato es igual
console.log(7 === 7);
console.log(7 === "7");

// Desigualdad ( != )
// Compara si el valor y el tipo de dato es desigual
console.log(7 != 7);
console.log(7 != "7");

// Desgualdad estricta ( === )
// Compara si el valor y el tipo de dato es desigual
console.log(6 !== 7);
console.log(6 !== "6");

